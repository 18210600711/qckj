package com.qckj.model;

public class MoneyBase {
private String id;
private int money;
private long time;
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public int getMoney() {
	return money;
}
public void setMoney(int money) {
	this.money = money;
}
public long getTime() {
	return time;
}
public void setTime(long time) {
	this.time = time;
}

}